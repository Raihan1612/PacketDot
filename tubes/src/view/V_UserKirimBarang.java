    private void jButtonPickUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPickUpActionPerformed
        // TODO add your handling code here:
        String namaKirim = jTextNamaPengirim.getText();
        String alamatKirim = jTextAlamatPengirim.getText();
        String kotaKirim = jTextKotaPengirim.getText();
        String teleponKirim = jTextTeleponPengirim.getText();
        String namaTerima = jTextNamaPenerima.getText();
        String alamatTerima = jTextAlamatPenerima.getText();
        String kotaTerima = jTextKotaPenerima.getText();
        String teleponTerima = jTextTeleponPenerima.getText();
        String namaBarang = jTextNamaBarang.getText();
        String beratBarang = jTextBeratBarang.getText();
        String jenisPengiriman = (String) jComboBoxJenis.getSelectedItem();
        
        try{
            if(namaKirim.equals("") || alamatKirim.equals("") || kotaKirim.equals("") || teleponKirim.equals("") || namaTerima.equals("") || alamatTerima.equals("") || kotaTerima.equals("") ||
                    teleponTerima.equals("") || namaBarang.equals("") || beratBarang.equals("")){
                JOptionPane.showMessageDialog(null, "Isi Semua Data!");
            } else{
                Barang b = new Barang(namaKirim, alamatKirim, kotaKirim, teleponKirim, namaTerima, alamatTerima, kotaTerima, teleponTerima, namaBarang, beratBarang, jenisPengiriman);
                String in = JOptionPane.showInputDialog(null, "Isi Pembayaran Sejumlah: Rp "+biaya());
                if (Float.parseFloat(in)==biaya()){
                    b.setBarang();
                    JOptionPane.showMessageDialog(null, "Barang Berhasil Diproses!");
                    V_UserCekBarang u =  new V_UserCekBarang();
                    u.setVisible(true);
                    setVisible(false);
                } else{
                    JOptionPane.showMessageDialog(null, "Pembayaran Tidak Sesuai!");
                }
            }
        } catch (Exception e){
            System.err.println("Error:"+e);
        }
    }//GEN-LAST:event_jButtonPickUpActionPerformed

