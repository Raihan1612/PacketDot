    public void setTextField(){
        try {
            Login l = new Login();
            ResultSet result = l.getUserData();
            if(result.next()){
                jTextNamaPengirim.setText(result.getString("nama"));
                jTextAlamatPengirim.setText(result.getString("alamat"));
                jTextKotaPengirim.setText(result.getString("kota"));
                jTextTeleponPengirim.setText(result.getString("telepon"));
            }
        } catch (SQLException ex) {
            System.err.println("Error : setTextField()");
        }
    }
    
    public int biaya(){
        int biaya;
        String kotaKirim = jTextKotaPengirim.getText();
        String kotaTerima = jTextKotaPenerima.getText();
        int beratBarang = Integer.parseInt(jTextBeratBarang.getText());
        String jenisPengiriman = (String) jComboBoxJenis.getSelectedItem();
        biaya = 10000;
        int biayaPerKilo = 2000;
        
        if ("Express".equals(jenisPengiriman)){
            biaya += 10000;
        }
        
        if (!kotaTerima.equals(kotaKirim)){
            biaya += 5000;
        }
        biaya += biayaPerKilo*beratBarang;
        
        return biaya;
    }
